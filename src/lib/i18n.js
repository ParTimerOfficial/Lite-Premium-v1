// Get current language from localStorage or default to Bangla
export const getCurrentLanguage = () => {
  return localStorage.getItem('language') || 'bn';
};

// Set current language
export const setLanguage = (language) => {
  localStorage.setItem('language', language);
};

// Get translation for key
export const t = (key) => {
  const lang = getCurrentLanguage();
  return translations[lang][key] || translations.en[key] || key;
};

// Get all translations for current language
export const getTranslations = () => {
  const lang = getCurrentLanguage();
  return translations[lang];
};

// Switch language
export const switchLanguage = (language) => {
  setLanguage(language);
  // Reload the page to apply new translations
  window.location.reload();
};

// Format currency with appropriate symbols
export const formatCurrency = (amount, currency = 'coins') => {
  const lang = getCurrentLanguage();
  if (currency === 'bdt') {
    return lang === 'bn' ? `${amount} টাকা` : `৳${amount}`;
  }
  return lang === 'bn' ? `${amount} কয়েন` : `${amount} Coins`;
};

// Format percentage
export const formatPercentage = (value) => {
  const lang = getCurrentLanguage();
  return lang === 'bn' ? `${value}%` : `${value}%`;
};